  const handleExportPdf = async () => {
    if (!reportContent) return;
    try {
      // 动态导入 html2pdf.js
      const html2pdf = (await import('html2pdf.js')).default;
      const { marked } = await import('marked');
      
      marked.setOptions({ gfm: true, breaks: true });
      const bodyHtml = await marked.parse(reportContent);
      const dateStr = new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' });
      
      const fullHtml = `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <title>Gracias AI — App Store Compliance Report</title>
  <style>
    body { font-family: -apple-system, BlinkMacSystemFont, "Segoe UI", Arial, sans-serif; font-size: 12px; line-height: 1.6; color: #1a1a2e; padding: 20px; max-width: 800px; margin: 0 auto; }
    .header { display: flex; justify-content: space-between; align-items: center; border-bottom: 2px solid #7c3aed; padding-bottom: 10px; margin-bottom: 20px; }
    .brand { display: flex; align-items: center; gap: 8px; }
    .logo { background: linear-gradient(135deg, #7c3aed, #3b82f6); width: 24px; height: 24px; border-radius: 6px; display: flex; align-items: center; justify-content: center; color: #fff; font-size: 12px; font-weight: 900; }
    .brand-name { font-size: 14px; font-weight: 800; color: #000; }
    .meta { text-align: right; font-size: 9px; color: #777; }
    h1 { font-size: 16px; font-weight: 900; color: #0f0f1a; margin: 18px 0 8px; border-bottom: 1px solid #e5e5f0; padding-bottom: 4px; }
    h2 { font-size: 14px; font-weight: 800; color: #0f0f1a; margin: 16px 0 6px; border-bottom: 1px solid #eee; padding-bottom: 3px; }
    h3 { font-size: 12px; font-weight: 700; color: #1a1a2e; margin: 12px 0 4px; }
    p { margin: 5px 0; color: #333; }
    ul, ol { margin: 5px 0 5px 16px; }
    li { margin: 2px 0; color: #444; }
    strong { font-weight: 700; color: #0f0f1a; }
    code { font-family: "SF Mono", Consolas, monospace; font-size: 10px; background: #f3f0ff; color: #7c3aed; padding: 1px 3px; border-radius: 3px; border: 1px solid #e9e5ff; }
    pre { background: #f8f8f8; border: 1px solid #e5e5e5; border-radius: 4px; padding: 8px; overflow-x: auto; margin: 8px 0; }
    pre code { background: none; border: none; padding: 0; color: #333; }
    blockquote { border-left: 3px solid #7c3aed; background: #faf8ff; margin: 8px 0; padding: 6px 10px; border-radius: 0 4px 4px 0; color: #444; }
    table { width: 100%; border-collapse: collapse; margin: 10px 0; font-size: 10px; border: 1px solid #e5e5f0; }
    thead { background: #f3f0ff; }
    th { padding: 5px 8px; text-align: left; font-size: 8px; font-weight: 700; text-transform: uppercase; color: #555; border-bottom: 1px solid #e0ddf8; }
    td { padding: 5px 8px; border-bottom: 1px solid #f0eeff; color: #333; }
    .badge { display: inline-flex; align-items: center; padding: 1px 6px; border-radius: 10px; font-size: 8px; font-weight: 700; }
    .badge-CRITICAL { background: #fee2e2; color: #b91c1c; border: 1px solid #fecaca; }
    .badge-HIGH { background: #ffedd5; color: #c2410c; border: 1px solid #fed7aa; }
    .badge-MEDIUM { background: #fefce8; color: #a16207; border: 1px solid #fde68a; }
    .badge-LOW { background: #eff6ff; color: #1d4ed8; border: 1px solid #bfdbfe; }
    .badge-PASS { background: #f0fdf4; color: #15803d; border: 1px solid #bbf7d0; }
    .footer { margin-top: 20px; padding-top: 8px; border-top: 1px solid #eee; font-size: 8px; color: #aaa; text-align: center; }
    @media print { body { padding: 12px; } @page { margin: 10mm; size: A4; } }
  </style>
</head>
<body>
  <div class="header">
    <div class="brand">
      <div class="logo">G</div>
      <div class="brand-name">Gracias AI — App Store Compliance Auditor</div>
    </div>
    <div class="meta">${dateStr}<br/>gracias.sh</div>
  </div>
  ${bodyHtml}
  <div class="footer">Generated by Gracias AI — gracias.sh</div>
  <script>
    document.querySelectorAll('td').forEach(td => {
      const text = td.textContent.trim();
      const badges = { CRITICAL: 'CRITICAL', HIGH: 'HIGH', MEDIUM: 'MEDIUM', LOW: 'LOW', PASS: 'PASS' };
      if (badges[text]) td.innerHTML = '<span class="badge badge-' + text + '">' + text + '</span>';
    });
  </script>
</body>
</html>`;
      
      const element = document.createElement('div');
      element.innerHTML = fullHtml;
      
      await html2pdf().set({
        margin: 10,
        filename: 'gracias-ai-audit-report-' + new Date().toISOString().slice(0, 10) + '.pdf',
        image: { type: 'jpeg', quality: 0.98 },
        html2canvas: { scale: 2, useCORS: true },
        jsPDF: { unit: 'mm', format: 'a4', orientation: 'portrait' }
      }).from(element).save();
    } catch (err) {
      console.error('PDF export failed:', err);
      setErrorMessage('Failed to export PDF. Please try Markdown export instead.');
    }
  };
